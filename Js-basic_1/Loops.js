// For loop
console.log("This is by for loop\n");
for (let i = 0; i <= 5; i++) {
    console.log(i);
}


// While loop

console.log("This is by while loop\n");

let i = 0;
while (i < 10) {
    console.log(i);

    i++;
}

// do-while loop

console.log("This is by do-while loop\n");

let j = 0;
do {
    console.log("ohm namh shivay", j);
    j++;
}
while (j < 10);
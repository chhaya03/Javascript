
//Arithmetic operator (+ - * / %  x^y=x**y)

console.log("Arithmetic operator:");

let a = 5;
let b = 2;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);   // ** means 5 power 2




// increment(x++ or ++x) and decrement operator (x-- or --x)

let x = 5;
console.log("increment  operator:");
console.log(x++);
console.log(++x);
console.log("decrement  operator:");
console.log(x--);
console.log(--x);


// comparison operator (> < >= <=  === !==)
console.log("This is comparison operator:")
console.log(5 > 3);
console.log(3 >= 5);
console.log(5 != 5);

